using System;

class Funcao{
    static void Main(){
        Console.WriteLine(Math.Pow(2,2)); //Potencia
        Console.WriteLine(Math.Sqrt(9)); //Raiz Quadrada

        Console.WriteLine(Math.Round(3.4)); //Arredonda o mais proximo
        Console.WriteLine(Math.Ceiling(3.4)); //Arredonda a cima
        Console.WriteLine(Math.Floor(3.9)); //Arredonda a baixo

        Console.WriteLine(Math.Max(3,7)); //Mostra o maior
        Console.WriteLine(Math.Min(3, 7)); //Mostra o menor
    }
}